package main

import (
	"fmt"
	"log"
	"math"
	"net/http"
	"os"
	"time"

	amqp "github.com/rabbitmq/amqp091-go"
)

const webPort = "80"

type rpcAPI int
type Config struct {
	RabbitPost *amqp.Connection
}

func main() {

	rabbitConnPost, err := connectRabbitPost()

	if err != nil {
		log.Println(err)
		os.Exit(1)
	}

	defer rabbitConnPost.Close()
	app := &Config{
		RabbitPost: rabbitConnPost,
	}

	log.Println("Connected to RabbitMQ")

	log.Printf("Starting API on port %s", webPort)

	srv := &http.Server{
		Addr:    fmt.Sprintf(":%s", webPort),
		Handler: app.routes(),
	}

	err = srv.ListenAndServe()

	if err != nil {
		log.Fatalf("Could not start server: %v", err)
	}
}

func connectRabbitPost() (*amqp.Connection, error) {
	var counts int64
	var backOff = 1 * time.Second
	var connection *amqp.Connection

	// don't continue until rabbit is ready
	for {
		c, err := amqp.Dial("amqp://guest:guest@rabbitmq")
		if err != nil {
			fmt.Println("RabbitMQ not yet ready...")
			counts++
		} else {
			log.Println("Connected to RabbitMQ!")
			connection = c
			break
		}

		if counts > 5 {
			fmt.Println(err)
			return nil, err
		}

		backOff = time.Duration(math.Pow(float64(counts), 2)) * time.Second
		log.Println("backing off...")
		time.Sleep(backOff)
		continue
	}

	return connection, nil
}

